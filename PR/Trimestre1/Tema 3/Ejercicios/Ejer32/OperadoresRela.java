class OperadoresRela{
	public static void main(String[] args){
		System.out.println("\t\t=========================");
		System.out.println("\t\t|OPERADORES RELACIONALES|");
		System.out.println("\t\t=========================");
		
		System.out.println("5 > 4 = " + (5 > 4));
		System.out.println("5 < 4 = " + (5 < 4));
		System.out.println("5 >= 4 = " + (5 >= 4));
		System.out.println("5 <= 4 = " + (5 <= 4));
		System.out.println("5 == 4 = " + (5 == 4));
		System.out.println("5 == 5 = " + (5 == 5));
		System.out.println("5 != 4 = " + (5 != 4));
		System.out.println("5 != 5 = " + (5 != 5));
	}
}
